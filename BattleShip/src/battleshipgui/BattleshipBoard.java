    /**
     * The same code for all the buttons in the game board 
     * 
     * @param evt 
     */
    private void jToggleButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jToggleButton1MouseClicked
        JToggleButton jt = ((JToggleButton) evt.getSource());       // get the button pressed in the board
        String tempStr = jt.getName();                              // get the buttons name
        //jt.setEnabled(false);
        String rowStr = tempStr.substring(1, 2);                    // to extract the coordinates
        String colStr = tempStr.substring(2);                       // from its name
        int row = Integer.valueOf(rowStr);                          // row
        int col = Integer.valueOf(colStr);                          // col
        if (!myGame.cell_taken(row, col) && user_guests>=0) {       // if first time in this button?
            if (user_guests >= 0) {                                 // if has lasers to play
                user_guests--;                                      // decrease the lasers
                if (myGame.process_user_guess(row, col)) {          //if hit
                    jt.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/explosion.png")));
                    jt.repaint();
                    Media.playExplosion();
                    Media.playComplaint();
                } else {                                            //if miss
                    jt.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/miss.png")));
                    jt.repaint();
                    Media.playMissShot();
                    Media.playmock();
                }                                                   // end hit or miss
            }
            if (user_guests < 0 || myGame.userWon()) {              // run out choices
                String Msg = "You lose this game";
                if (myGame.userWon()) {
                    Msg = "You have won!!";
                } else {
                    show_remainingships();
                }
                JOptionPane.showMessageDialog(rootPane, Msg+ ". Press the Battleship button for another game");
                allGameResults.add(myGame.Get_Board_StatuswithLegend());        // store the board
                jComboBox1.addItem(String.valueOf(allGameResults.size()));      // add another option to the combo
                show_game_board_i(allGameResults.size() - 1);                   // show the last game in the text area
            }                                                                   // end has choices
        } else {                                                                // not the first time here
            jt.setSelected(true);                                               // keep the button down while in game
            

        }        // TODO add your handling code here:           //end first time here
    }//GEN-LAST:event_jToggleButton1MouseClicked

