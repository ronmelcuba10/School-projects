    private void restore_theGameBoard() {
        myGame = new Battleship();
        myGame.clear_ship_locations();
        myGame.initialize_ship_locations();
        user_guests = 14;
        restore_jToggle_Buttons();

    }

    /**
     * Restores all the buttons to the initial state space image and unselected
     * Check for every component enlisted in jPanel1 if the component is a
     * JToggleButton then change image and state
     */
    private void restore_jToggle_Buttons() {
        for (int j = 0; j < jPanel1.getComponentCount(); j++) {
            if (jPanel1.getComponent(j).getClass().getName().contains("JToggleButton")) {
                ((JToggleButton) jPanel1.getComponent(j)).setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/space.png")));
                ((JToggleButton) jPanel1.getComponent(j)).setSelected(false);
                //((JToggleButton) jPanel1.getComponent(j)).setEnabled(true);
            }
        }
    }

    /**
     * Shows the remaining ships, if there are... 
     * the remaining ships from myGame is a list of Points. their coordinates match with the button name
     * if the button name matches the remaining ship coordinate then show a random ship from the
     * images
     */
    private void show_remainingships() {
        Random rnd_ship = new Random();
        Point[] ships = myGame.remaining_Ships();
        for (int i = 0; i < 7; i++) {
            for (int j = 0; j < jPanel1.getComponentCount(); j++) {
                if (jPanel1.getComponent(j).getName().equalsIgnoreCase("b" + ships[i].x + ships[i].y)) {
                    ((JToggleButton) jPanel1.getComponent(j)).setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/ship" + rnd_ship.nextInt(3) + ".png")));
                }

            }

        }
    }

    /**
     * Show the selected game in the text area
     *
     * @param selectedIndex
     */
    private void show_game_board_i(int selectedIndex) {
        jTextArea1.setText("");
        for (int i = 0; i < myGame.BOARD_LINES_W_LEGEND; i++) {
            jTextArea1.append(allGameResults.get(selectedIndex)[i] + "\n");
        }
    }

